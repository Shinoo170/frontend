import { useState, useEffect } from 'react'
import { useRouter } from 'next/router'
import Head from 'next/head'
import Image from 'next/image'
import styles from './wishlists.module.css'
import Header from 'components/header'
import SideNavBar from 'components/user/sideNavbar'
import axios from 'axios'
import ProductList from 'components/productList'

export default function Wishlists() {
    const [ isLogin, setIsLogin ] = useState(false)
    const router = useRouter()
    const [ wishlists, setWishlists ] = useState([])

    useEffect(() => {
        if(localStorage.getItem('jwt')){
            setIsLogin(true)
            getData()
        } else {
            router.push({pathname: '/', query:{ } }, undefined,{ shallow: false } )
        }
    }, [])

    const getData = () => {
        const url = process.env.NEXT_PUBLIC_BACKEND + '/user/getWishlists'
        axios.get(url, {
            headers: { jwt: localStorage.getItem('jwt')}
        })
        .then(result => {
            setWishlists(result.data)
        })
    }

    if(!isLogin) {
        return (
            <div>
                <Head>
                    <title>Wishlists | PT Bookstore</title>
                    <meta name="description" content="Generated by create next app" />
                    <link rel="icon" href="/favicon.ico" />
                </Head>
                <Header />
                <div className={styles.container}>
                    <main className={styles.main}>
                        
                    </main>
                </div>
            </div>
        )
    } else {
        return (
            <div>
                <Head>
                    <title>Wishlists | PT Bookstore</title>
                    <meta name="description" content="Generated by create next app" />
                    <link rel="icon" href="/favicon.ico" />
                </Head>
                <Header />
                <div className={styles.container}>
                    
                    <main className={styles.main}>
                        <SideNavBar />
                        <div className={styles.screen} >
                            <center><div className={styles.title}>รายการที่อยากได้</div></center>
                            <div>
                                { (wishlists[0] != undefined) && <ProductList data={wishlists} revert={false} maxPerPage='10' href='/series/'/> }
                            </div>
                        </div>
                    </main>
                </div>
            </div>
        )
    }
}